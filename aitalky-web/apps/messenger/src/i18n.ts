export type Lang = keyof typeof dict
export type I18nKey = keyof (typeof dict)['en_US']

let current: Lang = 'zh_CN'

// 设置生效语言。精确匹配语种码则用之;否则 zh* 归简体中文,其余一律英文兜底(国际通用)。
export function setLang(lang: string | null | undefined) {
  if (lang && lang in dict) {
    current = lang as Lang
    return
  }
  current = lang && lang.startsWith('zh') ? 'zh_CN' : 'en_US'
}

// 取文案。当前语种缺该 key 时回退英文,再退 key 本身(不至于显示 undefined)。
export function t(key: I18nKey): string {
  const table = dict[current] as Record<string, string>
  return table[key] ?? dict.en_US[key] ?? key
}
